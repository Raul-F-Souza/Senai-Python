#Desafio: crie um programa que calcule o perímetro e a área de um triângulo com base na altura e na base.

print("Desafio 04")

base = int(input('Digite a base do retângulo: '))
altura = int(input('Digite a altura do retângulo: '))

print(f'O perímetro do seu retângulo é {(base + base) + (altura + altura)} e área é {base * altura}')